sport = "Volleyball"
print (sport[0:6])
print (sport[0:-6]) # (-) character count the back
print (sport[-3 : -2]) # 7 : 8
print(sport[-6 : -4]) # 4: 6
print(sport [-3 : -1]) #7 :9
print(sport[-6]) # 10-6 = 4 show name 4 after character
print(sport[2:]) # slicing from end
print(sport[:2]) # slicing from start


